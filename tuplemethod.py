# Creating a sample tuple
tpl = (3, 1, 2, 2, 4)

# 1. count(value) - Returns the number of occurrences of a value in the tuple
print("Count of 2:", tpl.count(2))

# 2. index(value) - Returns the index of the first occurrence of a value
print("Index of 4:", tpl.index(4))

# 3. max() - Returns the largest element in the tuple
print("Max:", max(tpl))

# 4. min() - Returns the smallest element in the tuple
print("Min:", min(tpl))

# 5. len() - Returns the number of elements in the tuple
print("Length:", len(tpl))
